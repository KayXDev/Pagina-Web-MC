export function normalizeLang(value: string | undefined | null): Lang {
  if (value === 'en') return 'en';
  return 'es';
}

export function t(lang: Lang, key: string): string {
  const parts = key.split('.');
  let cur: any = DICT[lang];
  for (const p of parts) {
    cur = cur?.[p];
  }
  return typeof cur === 'string' ? cur : key;
}

export function getClientLangFromCookie(): Lang {
  if (typeof document === 'undefined') return 'es';
  const match = document.cookie.match(/(?:^|; )lang=([^;]*)/);
  const raw = match ? decodeURIComponent(match[1]) : '';
  return normalizeLang(raw);
}

export function getLangFromCookieStore(cookieStore: { get: (name: string) => { value: string } | undefined }): Lang {
  const raw = cookieStore.get('lang')?.value;
  return normalizeLang(raw);
}

export function getDateLocale(lang: Lang): string {
  return lang === 'en' ? 'en-US' : 'es-ES';
}
